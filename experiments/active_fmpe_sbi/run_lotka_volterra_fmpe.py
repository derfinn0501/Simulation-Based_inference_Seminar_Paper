#!/usr/bin/env python3
"""First FMPE-style Lotka-Volterra experiment.

This script keeps the same simulator and design interface as
`run_lotka_volterra.py`, but replaces the Gaussian posterior baseline with a
lightweight flow-matching posterior estimator.

The implementation is intentionally small:

- theta is standardized,
- theta_0 is Gaussian noise,
- theta_t = (1 - t) theta_0 + t theta_1,
- the network learns the rectified-flow vector field theta_1 - theta_0,
- posterior samples are generated by Euler-integrating the learned vector field.

This is not yet a production FMPE model, but it tests the project-specific idea:
can a conditional vector-field posterior estimator be dropped into the same
BO-guided simulation design loop?
"""

from __future__ import annotations

import argparse
import csv
import warnings
from collections import defaultdict
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
from sklearn.gaussian_process import GaussianProcessRegressor
from sklearn.gaussian_process.kernels import Matern, WhiteKernel
from sklearn.exceptions import ConvergenceWarning
from sklearn.neural_network import MLPRegressor
from sklearn.preprocessing import StandardScaler

from run_lotka_volterra import (
    append_data,
    decode_design,
    design_dim,
    sample_psi,
    sample_theta,
    simulate_batch,
)


def make_features(x: np.ndarray, psi: np.ndarray) -> np.ndarray:
    return np.hstack([x, psi])


class RectifiedFMPE:
    """Small conditional flow-matching posterior estimator."""

    def __init__(
        self,
        seed: int,
        flow_samples_per_pair: int = 4,
        hidden: tuple[int, int] = (128, 128),
        max_iter: int = 450,
    ):
        self.seed = seed
        self.flow_samples_per_pair = flow_samples_per_pair
        self.x_scaler = StandardScaler()
        self.theta_scaler = StandardScaler()
        self.model = MLPRegressor(
            hidden_layer_sizes=hidden,
            activation="relu",
            solver="adam",
            alpha=1e-4,
            batch_size="auto",
            learning_rate_init=2e-3,
            max_iter=max_iter,
            early_stopping=True,
            validation_fraction=0.15,
            n_iter_no_change=25,
            random_state=seed,
        )
        self.theta_dim: int | None = None

    def fit(self, x: np.ndarray, psi: np.ndarray, theta: np.ndarray) -> "RectifiedFMPE":
        rng = np.random.default_rng(self.seed)
        features = make_features(x, psi)
        features_std = self.x_scaler.fit_transform(features)
        theta_1 = self.theta_scaler.fit_transform(theta)
        self.theta_dim = theta_1.shape[1]

        n = theta_1.shape[0]
        k = self.flow_samples_per_pair
        idx = np.repeat(np.arange(n), k)
        theta_1_rep = theta_1[idx]
        feat_rep = features_std[idx]
        theta_0 = rng.normal(size=theta_1_rep.shape)
        t = rng.uniform(0.0, 1.0, size=(len(idx), 1))
        theta_t = (1.0 - t) * theta_0 + t * theta_1_rep
        target_velocity = theta_1_rep - theta_0
        model_input = np.hstack([theta_t, t, feat_rep])
        self.model.fit(model_input, target_velocity)
        return self

    def sample(
        self,
        x: np.ndarray,
        psi: np.ndarray,
        n_samples: int,
        steps: int,
        rng: np.random.Generator,
    ) -> np.ndarray:
        if self.theta_dim is None:
            raise RuntimeError("Model must be fit before sampling.")
        features = make_features(x, psi)
        features_std = self.x_scaler.transform(features)
        all_samples = []
        dt = 1.0 / steps
        for feat in features_std:
            theta_t = rng.normal(size=(n_samples, self.theta_dim))
            feat_rep = np.repeat(feat[None, :], n_samples, axis=0)
            for step in range(steps):
                t_value = np.full((n_samples, 1), step / steps)
                model_input = np.hstack([theta_t, t_value, feat_rep])
                velocity = self.model.predict(model_input)
                theta_t = theta_t + dt * velocity
            all_samples.append(self.theta_scaler.inverse_transform(theta_t))
        return np.asarray(all_samples)

    def posterior_mean(self, x: np.ndarray, psi: np.ndarray, n_samples: int, steps: int, seed: int) -> np.ndarray:
        rng = np.random.default_rng(seed)
        return self.sample(x, psi, n_samples=n_samples, steps=steps, rng=rng).mean(axis=1)


def fmpe_metrics(
    model: RectifiedFMPE,
    val: tuple[np.ndarray, np.ndarray, np.ndarray],
    n_samples: int,
    steps: int,
    seed: int,
) -> tuple[float, float]:
    theta_val, psi_val, x_val = val
    samples = model.sample(x_val, psi_val, n_samples=n_samples, steps=steps, rng=np.random.default_rng(seed))
    means = samples.mean(axis=1)
    rmse = float(np.sqrt(np.mean((means - theta_val) ** 2)))

    levels = (0.5, 0.8, 0.9)
    total = 0.0
    for level in levels:
        lo = np.quantile(samples, (1.0 - level) / 2.0, axis=1)
        hi = np.quantile(samples, 1.0 - (1.0 - level) / 2.0, axis=1)
        inside = (theta_val >= lo) & (theta_val <= hi)
        total += abs(float(np.mean(inside)) - level)
    coverage_error = total / len(levels)
    return rmse, coverage_error


def train_and_score_fmpe(
    train: tuple[np.ndarray, np.ndarray, np.ndarray],
    val: tuple[np.ndarray, np.ndarray, np.ndarray],
    seed: int,
    flow_samples_per_pair: int,
    posterior_samples: int,
    ode_steps: int,
) -> tuple[RectifiedFMPE, float, float]:
    theta_train, psi_train, x_train = train
    model = RectifiedFMPE(seed=seed, flow_samples_per_pair=flow_samples_per_pair).fit(x_train, psi_train, theta_train)
    rmse, cov_err = fmpe_metrics(model, val, n_samples=posterior_samples, steps=ode_steps, seed=seed + 123)
    return model, rmse, cov_err


def propose_design(
    rng: np.random.Generator,
    tried_psi: list[np.ndarray],
    rewards: list[float],
    n_candidates: int,
    design_space: str,
    exploit_best_prob: float,
) -> np.ndarray:
    if rewards and rng.uniform() < exploit_best_prob:
        return tried_psi[int(np.argmax(rewards))]
    if len(rewards) < 4:
        return sample_psi(rng, 1, design_space)[0]
    x_train = np.asarray(tried_psi)
    y_train = np.asarray(rewards)
    y_std = (y_train - y_train.mean()) / (y_train.std() + 1e-8)
    dim = design_dim(design_space)
    kernel = Matern(length_scale=np.ones(dim) * 0.4, nu=2.5) + WhiteKernel(noise_level=1e-3)
    gp = GaussianProcessRegressor(kernel=kernel, normalize_y=False, random_state=int(rng.integers(1e9)))
    with warnings.catch_warnings():
        warnings.simplefilter("ignore", ConvergenceWarning)
        gp.fit(x_train, y_std)
    best_psi = tried_psi[int(np.argmax(rewards))]
    candidates = np.vstack([sample_psi(rng, n_candidates, design_space), best_psi[None, :], np.asarray(tried_psi)])
    mean, std = gp.predict(candidates, return_std=True)
    return candidates[int(np.argmax(mean + 1.5 * std))]


def random_run(args: argparse.Namespace, val: tuple[np.ndarray, np.ndarray, np.ndarray], rep_seed: int, rep: int):
    rng = np.random.default_rng(rep_seed + 1)
    train = simulate_batch(
        sample_theta(rng, args.initial),
        sample_psi(rng, args.initial, args.design_space),
        rng,
        args.n_obs,
        args.noise_std,
        args.design_space,
    )
    rows = []
    for r in range(args.rounds + 1):
        _, rmse, cov_err = train_and_score_fmpe(
            train,
            val,
            seed=rep_seed + 3000 + r,
            flow_samples_per_pair=args.flow_samples_per_pair,
            posterior_samples=args.posterior_samples,
            ode_steps=args.ode_steps,
        )
        rows.append(
            {
                "method": "random_fmpe",
                "replicate": rep,
                "round": r,
                "simulations": len(train[0]),
                "posterior_mean_rmse": rmse,
                "coverage_error": cov_err,
            }
        )
        if r == args.rounds:
            break
        extra = simulate_batch(
            sample_theta(rng, args.batch),
            sample_psi(rng, args.batch, args.design_space),
            rng,
            args.n_obs,
            args.noise_std,
            args.design_space,
        )
        train = append_data(train, extra)
    return rows


def bo_run(args: argparse.Namespace, val: tuple[np.ndarray, np.ndarray, np.ndarray], rep_seed: int, rep: int):
    rng = np.random.default_rng(rep_seed + 2)
    train = simulate_batch(
        sample_theta(rng, args.initial),
        sample_psi(rng, args.initial, args.design_space),
        rng,
        args.n_obs,
        args.noise_std,
        args.design_space,
    )
    rows = []
    trace = []
    tried_psi: list[np.ndarray] = []
    rewards: list[float] = []
    _, old_rmse, old_cov = train_and_score_fmpe(
        train,
        val,
        seed=rep_seed + 4000,
        flow_samples_per_pair=args.flow_samples_per_pair,
        posterior_samples=args.posterior_samples,
        ode_steps=args.ode_steps,
    )
    rows.append(
        {
            "method": "bo_fmpe",
            "replicate": rep,
            "round": 0,
            "simulations": len(train[0]),
            "posterior_mean_rmse": old_rmse,
            "coverage_error": old_cov,
        }
    )
    for r in range(1, args.rounds + 1):
        psi_next = propose_design(
            rng,
            tried_psi,
            rewards,
            args.bo_candidates,
            args.design_space,
            args.exploit_best_prob,
        )
        n_random = int(round(args.batch * args.bo_random_fraction))
        n_focused = max(1, args.batch - n_random)
        psi_batch = np.repeat(psi_next[None, :], n_focused, axis=0)
        if n_random > 0:
            psi_batch = np.vstack([psi_batch, sample_psi(rng, n_random, args.design_space)])
        extra = simulate_batch(
            sample_theta(rng, len(psi_batch)),
            psi_batch,
            rng,
            args.n_obs,
            args.noise_std,
            args.design_space,
        )
        candidate_train = append_data(train, extra)
        _, new_rmse, new_cov = train_and_score_fmpe(
            candidate_train,
            val,
            seed=rep_seed + 4000 + r,
            flow_samples_per_pair=args.flow_samples_per_pair,
            posterior_samples=args.posterior_samples,
            ode_steps=args.ode_steps,
        )
        reward = old_rmse - new_rmse
        tried_psi.append(psi_next)
        rewards.append(reward)
        design = decode_design(psi_next, design_space=args.design_space)
        trace_row = {
            "replicate": rep,
            "round": r,
            "reward_delta_negative_rmse": reward,
            "prey0": design.prey0,
            "pred0": design.pred0,
            "t_start": design.t_start,
            "t_span": design.t_span,
        }
        for i, value in enumerate(psi_next):
            trace_row[f"psi{i}_unit"] = float(value)
        trace.append(trace_row)
        train = candidate_train
        old_rmse = new_rmse
        old_cov = new_cov
        rows.append(
            {
                "method": "bo_fmpe",
                "replicate": rep,
                "round": r,
                "simulations": len(train[0]),
                "posterior_mean_rmse": new_rmse,
                "coverage_error": new_cov,
            }
        )
    return rows, trace


def write_csv(path: Path, rows: list[dict]) -> None:
    if not rows:
        return
    fieldnames = sorted({key for row in rows for key in row.keys()})
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def plot(rows: list[dict], output: Path) -> None:
    plt.figure(figsize=(8, 5))
    for method in sorted({row["method"] for row in rows}):
        subset = [row for row in rows if row["method"] == method]
        grouped: dict[int, list[float]] = defaultdict(list)
        for row in subset:
            grouped[int(row["simulations"])].append(float(row["posterior_mean_rmse"]))
        xs = sorted(grouped)
        means = np.array([np.mean(grouped[x]) for x in xs])
        stds = np.array([np.std(grouped[x]) for x in xs])
        plt.plot(xs, means, marker="o", label=method)
        if any(len(grouped[x]) > 1 for x in xs):
            plt.fill_between(xs, means - stds, means + stds, alpha=0.18)
    plt.xlabel("Simulator calls")
    plt.ylabel("Posterior mean RMSE (lower is better)")
    plt.title("FMPE-style Lotka-Volterra active design")
    plt.grid(alpha=0.25)
    plt.legend()
    plt.tight_layout()
    plt.savefig(output, dpi=160)
    plt.close()


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--design-space", choices=("window", "hard_window", "full"), default="hard_window")
    parser.add_argument("--initial", type=int, default=80)
    parser.add_argument("--batch", type=int, default=12)
    parser.add_argument("--rounds", type=int, default=5)
    parser.add_argument("--validation", type=int, default=120)
    parser.add_argument("--repeats", type=int, default=2)
    parser.add_argument("--n-obs", type=int, default=10)
    parser.add_argument("--noise-std", type=float, default=0.06)
    parser.add_argument("--bo-candidates", type=int, default=128)
    parser.add_argument("--exploit-best-prob", type=float, default=0.25)
    parser.add_argument("--bo-random-fraction", type=float, default=0.25)
    parser.add_argument("--flow-samples-per-pair", type=int, default=4)
    parser.add_argument("--posterior-samples", type=int, default=64)
    parser.add_argument("--ode-steps", type=int, default=16)
    parser.add_argument("--seed", type=int, default=303)
    parser.add_argument("--output-dir", type=Path, default=Path(__file__).parent / "outputs_fmpe")
    parser.add_argument("--quick", action="store_true")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    if args.quick:
        args.initial = 45
        args.batch = 8
        args.rounds = 2
        args.validation = 50
        args.repeats = 1
        args.posterior_samples = 32
        args.ode_steps = 10

    args.output_dir.mkdir(parents=True, exist_ok=True)
    rows = []
    trace = []
    for rep in range(args.repeats):
        rep_seed = args.seed + 10_000 * rep
        rng = np.random.default_rng(rep_seed)
        val = simulate_batch(
            sample_theta(rng, args.validation),
            sample_psi(rng, args.validation, args.design_space),
            rng,
            args.n_obs,
            args.noise_std,
            args.design_space,
        )
        rows.extend(random_run(args, val, rep_seed, rep))
        bo_rows, bo_trace = bo_run(args, val, rep_seed, rep)
        rows.extend(bo_rows)
        trace.extend(bo_trace)

    write_csv(args.output_dir / "metrics.csv", rows)
    write_csv(args.output_dir / "bo_trace.csv", trace)
    plot(rows, args.output_dir / "posterior_rmse.png")
    print(f"Wrote metrics to {args.output_dir / 'metrics.csv'}")
    print(f"Wrote BO trace to {args.output_dir / 'bo_trace.csv'}")
    print(f"Wrote plot to {args.output_dir / 'posterior_rmse.png'}")
    print("Final rows:")
    for row in rows:
        if int(row["round"]) == args.rounds:
            print(row)


if __name__ == "__main__":
    main()
